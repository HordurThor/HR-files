class Node:
    def __init__(self, data = None, next = None):
        self.data = data
        self.next = next

def print_to_screen(head):
    if head != None:
        print(head.data, end=" ")
        print_to_screen(head.next)
    else:
        print("")

def get_size(head):
    if head == None:
        return 0
    return get_size(head.next)+1

def reverse_list(head):
    if head == None:
        return None
    if head.next == None:
        return head
    head1 = reverse_list(head.next)
    head.next.next = head
    head.next = None
    return head1

class PalindromeHelper:

    def __init__(self, node):
        self.node = node

def palindrome_helper(head, head2):
    if head == None:
        return True
    answer = palindrome_helper(head.next, head2)
    if not answer:
        return False
    if head.data != head2.node.data:
        return False
    head2.node = head2.node.next
    return True
    
def palindrome(head):
    return palindrome_helper(head, PalindromeHelper(head))

